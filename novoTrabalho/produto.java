public class produto{

    private string nome;
    private boolean preco;

    public string getNome() {
        return nome;
    }
    public void setNome(string nome) {
        this.nome = nome;
    }
    public boolean getPreco() {
        return preco;
    }
    public void setPreco(boolean preco) {
        this.preco = preco;
    }

}

